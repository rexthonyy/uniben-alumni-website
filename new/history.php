				<p class="rex-mt-32px rex-line-height-normal rex-fs-normal">
					Some members of the branch have served while some others are still serving on the national body at executive level.

					In order to meet the yearnings and aspiration of the association, the Delta State govt donated a parcel of land to the body located at Mariam Babangida Road by Board of Internal revenue.
					Construction work has commenced in the site and the structure is at decking level. The structure, upon completion, apart from becoming the Secretariat and meeting venue, it contains two multipurpose halls that will attract income to the association. The facility will also provide temporary accommodation for graduates of University of Benin on Youth service in Asaba and environs before they settle down.

					The association operates a committee system. Through the welfare committee, the body has establish a cooperative society, while the employment committee has facilitated the employment of young graduates from the university. During the Covid -19 Pandemic, the provided palliatives to the elderly and very needy amongst us.

					As part of our yearly activities, Prof E. A. Nwanze was our Guest Speaker for 2019 on the topic  Patriotism, National Ethos and Sustainable Development: The need for quality mindset.

