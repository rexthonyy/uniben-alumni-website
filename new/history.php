				<h1 class="rex-fs-large">In the beginning</h1>
				<p class="rex-mt-32px rex-line-height-normal rex-fs-normal">
					The University of Benin Alumni Association, Asaba Branch was founded in 1997 as a 2-man Association by Mr. Israel Odibeli and Engr. Solomon S. Onogure. Both of them were resident in Ibusa near Asaba and met in the evenings to strategise on how to get members to join the Association especially in Asaba which is a major hub of residents of graduates from the University.
					After a list of known members had been drawn up, they embarked on membership. A major breakthrough came when Chief (Pharm.) Dennis Amafor, who gave full support to the move.
					Mr. Victor Ochunor, then Secretary of the Delta State Branch of the Nigerian Union of Teachers, himself an Alumnus, offered the use of their Conference room for the monthly meetings. Refreshments then were sometimes provided for members in the form of biscuits and mineral drinks. Attendance at meetings were poor as persons seen in one meeting were absent at the next. There were times when only Mr. Israel Odibeli and Engr. Solomon S. Onogure would be present at a monthly meeting.
					Following the movement of the NUT from their rented office building opposite St. Patricks College, to their permanent site in 1998, a benevolent Alumnus who was a Vice Principal at the school offered the use of a classroom for the monthly meetings. At this time, hosting of meetings by individuals was out of the plan as they felt that members who were not financially sound would shy away from meetings. Members later volunteered to host in the central meeting place.
