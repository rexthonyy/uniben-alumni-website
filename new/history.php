					Later, Chief Amafor offered the use of a large hall at the top of his Pharmacy, Helt Hospitale near the Fedral Medical Centre. This better venue for meeting saw a growth of membership in the Association.  Membership however, surged with the coming of the civilian administration in 1999 which witnessed the influx of many graduates of the University both as key officials in Government and new recruits in the Civil Service and Public Service.
					Sequel to this monumental increase in membership, there was then the need for a lager venue for meetings. Thus, meetings were shifted to Nelrose Hotel. With the recurrent issue of poor finance, meetings were then hosted by members on rotational basis with their choice of venue. This is the position till date.
					Like an acorn, the University of Benin Alumni Association, Asaba Branch, has grown into an oak. The Branch has over these years have blossomed with some achievements including:
					Regular and well attended monthly meetings on rotational basis, hosted by members on every second Wednesday of the month at 4pm.
					Regular and well attended Exco meetings on rotational basis, hosted by Exco members.
					Meet our members social demands and entitlements in respect of social outings in which we were invited. 
					Have a workable Bye-Law that guides activities and conduct of members.
					The Branch over the years, carries out academic programmes to impact her immediate environment. It started initially as 'Schools Challenge' now 'Schools Reward for Excellence' targeted at Secondary Schools in Asaba and environs. 
					The Branch has hosted the NEC of the global body.
